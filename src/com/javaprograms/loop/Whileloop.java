package com.javaprograms.loop;

public class Whileloop {
	

	public static void main(String[] args) {
		
		int sal[]= {1000,200,500,600,9000};
		
		int i=0;
		
		while (i<sal.length) {
			
			if(sal[i]>=1000) {
				System.out.println(sal[i]);
			}			
			i++;
		}
		
		
		
		System.out.println("Ramesh"+";"+"Ravana"+";");
		
		
		
	}
}
